<h1 class="section_title">What do Molecular Orbitals look like?</h1>
<p>
Let's take the example of <b>thymine molecule, which is one the four DNA bases</b>. Thymine is a molecule consisting of 15 atoms; 5 carbon atoms, 2 nitrogen atoms, 2 oxygen atoms and 6 hydrogen atoms. The identity of the thymine molecule comes from how the 15 atoms are connected. Thymine has 66 electrons and thus 33 filled molecular orbitals (remember that each molecular orbital can only contain 2 electrons, one with spin up and one with spin down). Molecular Orbitals are in general spread out over several atoms. One specific Molecular Orbital of the thymine molecule is depicted below.
</p>
<br>
<p style="text-align:center;"><img src="http://education.natural-europe.eu/natural_europe/archive/fullsize/question-1-photo-3_7cd68e34d8.jpg"/></p>
<p><i>Chemical Structure of <a href="">thymine</a>. The balls represent the nuclei and the ensemble of green and red forms represent the shape of a single molecular orbital.</i></p>
<br>
<p>
The different green and red forms visible in the figure above belong to a single Molecular Orbital. These are not solid objects, like balloons, but represent that the probability to find an electron within this part of the space. The green and red colors correspond to a quantum property which cannot be described in a simple hand waving manner. A molecule has many orbitals, each one having its own shape.
</p>
