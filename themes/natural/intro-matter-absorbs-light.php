<h1 class="section_title">How is light absorbed by matter?</h1>
<p>
Matter may come in many forms such as gas, liquids, crystals or solids. Common to all forms of matter is that it is built of , themselves built of <a href="javascript:void(0);" onClick="getItemDef(this)" id="Nucleus (atomic) (glossary)">nuclei</a> and <a href="javascript:void(0);" onClick="getItemDef(this)" id="Electron (glossary)">electrons</a>. The atoms may be bound together into , the basis of chemistry.
</p>
<p>
When molecules are exposed to <a href="javascript:void(0);" onClick="getItemDef(this)" id="Light (glossary)">light</a>, which is a sort of energy, they may absorb it under certain conditions. Thus the energy of the light is transferred to the electrons which now behave in a new way. 
Photochemists call this new energy state of the molecule an <a href="intro-electronic-state">Excited Electronic State</a>. An excited electronic state cannot contain any arbitrary amount of energy but only certain discreet values (this is described by <a href="javascript:void(0);" onClick="getItemDef(this)" id="Quantum physics (glossary)">quantum physics</a>). 
Therefore, a molecule can only absorb <a href="javascript:void(0);" onClick="getItemInfo(this)" id="Photon">photons</a> with the corresponding quantum of energy. 
</p>
<p>
An important property of molecules is that the energy of the <a href="javascript:void(0);" onClick="getItemInfo(this)" id="Photon">photons</a> they can absorb may depend on their environment, e.g. if this is a water solution, or an organic solvent such as hydrocarbon, ethanol etc. 
</p>
