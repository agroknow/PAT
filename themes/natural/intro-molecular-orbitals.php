<h1 class="section_title">What is a Molecular Orbital?</h1>
<p>
In order to describe the behavior of electrons within a molecule, scientists use the concept of Molecular Orbitals. As in the case of atoms; molecules have a certain number of them filled with electrons (called occupied orbitals), and others that are empty. Electrons fill up the orbitals, starting with those having the lowest energies. This leads to the most stable electronic state called the <a href="intro-ground-electronic-state">Ground Electronic State</a> of the molecule.
</p>
<p>
In order to represent in a simple way the different Molecular Orbitals and the way that they are filled with electrons, chemists draw horizontal lines. These are ordered according to their energy, starting from the one with least energy and upwards with increasing energy.Each electron is represented by a arrow, the direction of which, up or down, depicts the electron spin, a concept coming from quantum physics. Each Molecular Orbital can only contain two electrons, one with spin up and one with spin down. </p>
<p>
As an example, the figure below represents the water molecule (H2O) which contains 10 electrons (2 from the 2 hydrogen atoms and 8 from the oxygen atom). These 10 electrons are allocated in five Molecular Orbitals corresponding to the <a href="">Ground Electronic State</a> of the water molecule. </p>
<p style="text-align:center;"><img src="http://education.natural-europe.eu/natural_europe/archive/fullsize/question-1-photo-2_d7238781d1.jpg"/></p>
<p>When the molecule acquires energy by absorbing light, an electron moves from a filled orbital to an empty one. This corresponds to an <a href="intro-ground-electronic-state">Electronic Excitation</a> from the <a href="intro-ground-electronic-state">Ground Electronic State</a> to an <a href="intro-ground-electronic-state">Excited Electronic State</a>.</p>
