<h1 class="section_title">Ground Electronic State</h1>
<p>
Each Molecular Orbital, either occupied or empty, corresponds to a given energy. Remember that <a href="javascript:void(0);" onClick="getItemDef(this)" id="Electron (glossary)">electrons</a> fill up the most stable orbitals, i.e. the orbitals with the lowest energies. When all the electrons have taken their places in the low-energy orbitals, this leads to the most stable electronic state, also called the Ground Electronic State of the molecule. </p>
<p>When the molecule receives an extra amount of energy by absorbing <a href="javascript:void(0);" onClick="getItemDef(this)" id="Light (glossary)">light</a>, one of its <a href="javascript:void(0);" onClick="getItemDef(this)" id="Electron (glossary)">electrons</a> may move from a filled orbital to an empty one. This is called an Electronic Excitation. Since the electron, and thus the molecule as a whole, now possesses more energy, the molecule finds itself in an Excited electronic state.</p>
<p>
In the Ground Electronic State, <a href="intro-molecular-orbitals-chemical-bond">Chemical Bonds</a> are stronger, while in an Excited Electronic State they are weaker.
</p>
<p>
As an example we use the orbitals of the <a href="javascript:void(0);" onClick="getItemDef(this)" id="Thymine (glossary)">thymine</a> molecule. Thymine has 66 <a href="javascript:void(0);" onClick="getItemDef(this)" id="Electron (glossary)">electrons</a> and thus, when it is in its Ground Electronic State, 33 orbitals are filled. 
</p>
