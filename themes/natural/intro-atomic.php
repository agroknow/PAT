<p>
(<a href="http://www.magnet.fsu.edu/education/tutorials/magnetacademy/superconductivity101/images/superconductivity-aluminum.jpg"><span style="font-family: Calibri;">http://www.magnet.fsu.edu/education/tutorials/magnetacademy/superconductivity101/images/superconductivity-aluminum.jpg</span></a>) 
</p>


<p>This perception of the electrons and their electronic behaviour has its origins in the work of several scientists working in the first years of last century, such as <a href="http://en.wikipedia.org/wiki/Niels_Bohr" title="Niels Bohr">Niels Bohr</a>.</p>

<p>In the 20s, due to the better understanding of the laws governing the microscopic behavior of the matter, i.e. the quantum mechanics, the scientists gradually start thinking that electrons have a dual particle/wavelike nature. In other words, some phenomena can be better understood considering that electrons are solid particles and other that they behave such a wave. Furthermore, several experiments show that the simple picture of an atom as a small solar system was wrong. 
</p>

<p >The term &lsquo;orbit&rsquo; describing previously the behaviour of electrons in the atom, has been replaced with the term <b ><i>orbital</i></b>&mdash;a term first coined by chemist <a href="http://en.wikipedia.org/wiki/Robert_Mulliken" title="Robert Mulliken">Robert Mulliken</a> in 1932. 
</p>

<p >The main difference is that the electron is not to be found in a specific position anymore. The scientists use mathematical description in defining the electron orbitals. They describe these orbitals as <b>three-dimensional probability zones (i.e. orbitals) distributed around the atomic nucleus. These zones describe the electrons most likely location.</b>
</p>

<p >The shape of one particular atomic orbital is schematically depicted below. This is the atom of <b>Hydrogen</b>, which contains one electron revolving around the nucleus. We can see the electron orbital as a three-dimensional spherical cloud surrounding the nucleus. In Quantum Physics an orbital with this shape is called an &ldquo;s -orbital&rdquo;.
</p>

<p><img height="99" width="120" src="<?php echo uri('themes/natural/images/intropages/hatomorbitals.png');  ?>" alt="" /></span></p>

<p>Orbitals and energy </p>
<p >An important characteristic of an orbital is that we can associate a specific energy to it. This means that when an electron moves within a particular orbital, it bears a specific amount of energy.</p>
<p >Each orbital may contain a maximum of two electrons. In other words, a maximum of two electrons can be &lsquo;described&rsquo; by the same orbital. </p>
<p >If an orbital contains only one electron, it is considered half filled, such as the <a title="Hydrogen atom" href="http://en.wikipedia.org/wiki/Hydrogen_atom">hydrogen atom</a> depicted above. An orbital is empty when it contains no electrons. This is how a Hydrogen electron orbital of higher energy looks like. </p>

<p ><img height="124" width="192" border="0" src="<?php echo uri('themes/natural/images/intropages/p-orbital.bmp');  ?>" alt="" /></p>

<p >This means that the Hydrogen electron can now be found in a more remote probability zone, i.e. atomic orbital. You may notice that the shape is not spherical anymore, but it looks like a dumbbell. In Quantum Physics an orbital with this shape is a typical &ldquo;p - orbital&rdquo;.</p>

<p >The picture becomes more complicated for atoms that contain more than one electron. </p>

<p >Electrons are prone to filling up the most stable orbitals, i.e. the orbitals with the lower energy levels. When this happens it leads to producing the most stable electronic state possible, called <b >Ground</b><b > Electronic State</b> of the atom. </p>


