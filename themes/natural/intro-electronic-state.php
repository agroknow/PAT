<h1 class="section_title">What is an Excited Electronic State?</h1>
<p>
In the absence of external perturbations, a molecule normally exists in its most stable condition, called the <a href="intro-ground-electronic-state">Ground Electronic State</a>.
</p>
<p>
When the molecule takes up more energy than usual, it enters a so called Excited Electronic State. There are many different excited electronic states, depending on the amount of energy absorbed by the <a href="javascript:void(0);" onClick="getItemDef(this)" id="Molecule (glossary)">molecule</a>. The change from the ground to an excited state is called <a href="intro-ground-electronic-state">Electronic Excitation</a>.
</p>
<p>This process can be illustrated as in the figure below where the chemical structure of the <a href="javascript:void(0);" onClick="getItemDef(this)" id="Thymine (glossary)">thymine</a> molecule, one of the four DNA bases, is shown.
</p>
<p><img width="420px" src="http://education.natural-europe.eu/natural_europe/archive/fullsize/question-1-photo_cec7113c07.jpg"/></p>
<p>
To better understand this process, and what an Excited Electronic State really is, scientists use the concept of electronic orbitals. The electrons of atoms move very rapidly within a more a less defined space around the nucleus, called Atomic Orbitals. In the case of molecules, the term of <b><a href="intro-molecular-orbitals">Molecular Orbitals</a> is used.</b>
</p>

