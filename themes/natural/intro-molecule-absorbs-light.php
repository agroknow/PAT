<h1 class="section_title">What happens to the molecule when it absorbs light?</h1>
<p>
Once a molecule absorbs light and gets into the <a href="intro-electronic-state">excited electronic state</a>, it does not want to stay there. It simply wants to get rid of the excess energy. At the end of this process, called excited <b>state deactivation</b>, the molecule may remain the same as it was before light absorption or undergo a chemical change.
</p>
<p>
Below you may find the illustration of some ways by which a molecule could loose the energy acquired by absorption of a photon but without loosing its identity.
The energy of light may be dissipated as heat to the environment of the molecule. This is explained in the digital exhibit "<a href="http://education.natural-europe.eu/natural_europe/exhibits/your-dna-gets-a-suntan/to-begin-with">building blocks of life under UV irradiation</a>".
</p>
<p><img src="http://education.natural-europe.eu/natural_europe/archive/fullsize/trel-piscine_557c872040.jpg"/></p>
<p><i>TREL the molecule transfers very rapidly the energy acquired by the absorption of a photon to the surrounding water. In this way, the molecule avoids undergoing chemical reactions and remains intact.</i></p>
<p>Alternatively, the molecule may emit light, but of a different colour (<a href="javascript:void(0);" onClick="getItemDef(this)" id="luminescence">Luminescence</a>).</p>
<p><img src="http://education.natural-europe.eu/natural_europe/archive/files/trel-fluorescence_e8cb4e1020.gif" width="470px"/></p>
<p><i>TREL the molecule absorbs a green photon. After using a small part of the photon energy, which makes him shake (vibrations) it emits a new photon (fluorescence). This photon is red because it contains less energy than the green one.</i></p>
<p><img src="http://education.natural-europe.eu/natural_europe/archive/fullsize/trel-fluorescence-lifetime_252cd5f70b.jpg"/></p>
<p>The time during which light is emitted varies largely from one molecule to the other. For the DNA macromolecule, it depends on the motions between its deffernt parts (called conformational motions). The larger the motions the more rapidly photons are emitted. 1ps corresponds to 10<sup style="font-size:70%; vertical-align:super;">-12</sup> seconds and 1ns to10<sup style="font-size:70%; vertical-align:super;">-9</sup> sec.</p>
